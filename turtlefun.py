import turtle

finn = turtle.clone()
turtle.hideturtle()
finn.shape("arrow")
finn.pensize(2)
finn.pencolor("green")
finn.goto(0,0)
finn.goto(0,-100)
finn.right(90)
finn.goto(10,-100)
finn.pencolor("blue")
finn.right(90)
finn.goto(10,0)

finn.bgcolor("purple")


























turtle.mainloop()
